#ifndef OpenSMOKE_Grammar_LookUpTable_ZY_H
#define OpenSMOKE_Grammar_LookUpTable_ZY_H

#include "dictionary/OpenSMOKE_DictionaryManager.h"
#include "dictionary/OpenSMOKE_DictionaryGrammar.h"
#include "dictionary/OpenSMOKE_DictionaryKeyWord.h"

namespace OpenSMOKE
{
	class Grammar_LookUpTable_ZY : public OpenSMOKE::OpenSMOKE_DictionaryGrammar
	{
	protected:

		virtual void DefineRules()
		{
			AddKeyWord(OpenSMOKE::OpenSMOKE_DictionaryKeyWord("@Path",
				OpenSMOKE::SINGLE_PATH,
				"Path to the lookup table (XML format)",
				true));

			AddKeyWord(OpenSMOKE::OpenSMOKE_DictionaryKeyWord("@Apriori",
				OpenSMOKE::SINGLE_BOOL,
				"If true, a simple a priori application of the table is applied (default: true)",
				true));

			AddKeyWord(OpenSMOKE::OpenSMOKE_DictionaryKeyWord("@Fields",
				OpenSMOKE::VECTOR_STRING,
				"Fields for which to read the lookup tables",
				true));

			AddKeyWord(OpenSMOKE::OpenSMOKE_DictionaryKeyWord("@Initialization",
				OpenSMOKE::SINGLE_STRING,
				"Initialization type: FromScratch | FromBackup | FromReconstruction | FromOnTheFlySolution",
				true));
		}
	};
}

#endif /* OpenSMOKE_Grammar_LookUpTable_ZY_H */